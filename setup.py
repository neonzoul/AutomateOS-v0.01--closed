#!/usr/bin/env python3
"""
AutomateOS Setup Script

This script helps set up AutomateOS for development or production.
It handles environment configuration, dependency installation, and basic setup.
"""

import os
import sys
import subprocess
import shutil
import secrets
from pathlib import Path

def run_command(command, cwd=None, check=True):
    """Run a shell command and return the result."""
    try:
        result = subprocess.run(
            command, 
            shell=True, 
            cwd=cwd, 
            check=check,
            capture_output=True,
            text=True
        )
        return result
    except subprocess.CalledProcessError as e:
        print(f"‚ùå Command failed: {command}")
        print(f"Error: {e.stderr}")
        if check:
            sys.exit(1)
        return e

def check_python_version():
    """Check if Python version is 3.9 or higher."""
    version = sys.version_info
    if version.major < 3 or (version.major == 3 and version.minor < 9):
        print("‚ùå Python 3.9 or higher is required")
        print(f"Current version: {version.major}.{version.minor}.{version.micro}")
        sys.exit(1)
    print(f"‚úÖ Python {version.major}.{version.minor}.{version.micro} detected")

def check_node_version():
    """Check if Node.js is installed and version is 18 or higher."""
    try:
        result = run_command("node --version", check=False)
        if result.returncode != 0:
            print("‚ùå Node.js is not installed")
            print("Please install Node.js 18 or higher from https://nodejs.org/")
            return False
        
        version_str = result.stdout.strip().lstrip('v')
        major_version = int(version_str.split('.')[0])
        
        if major_version < 18:
            print(f"‚ùå Node.js 18 or higher is required (found {version_str})")
            return False
        
        print(f"‚úÖ Node.js {version_str} detected")
        return True
    except Exception as e:
        print(f"‚ùå Error checking Node.js version: {e}")
        return False

def check_redis():
    """Check if Redis is available."""
    try:
        result = run_command("redis-cli ping", check=False)
        if result.returncode == 0 and "PONG" in result.stdout:
            print("‚úÖ Redis is running")
            return True
        else:
            print("‚ö†Ô∏è  Redis is not running")
            return False
    except Exception:
        print("‚ö†Ô∏è  Redis is not installed or not running")
        return False

def create_env_file(environment="development"):
    """Create .env file with default configuration."""
    env_path = Path(".env")
    
    if env_path.exists():
        response = input("üìù .env file already exists. Overwrite? (y/N): ")
        if response.lower() != 'y':
            print("Skipping .env file creation")
            return
    
    # Generate a secure secret key
    secret_key = secrets.token_urlsafe(32)
    
    env_content = f"""# AutomateOS Environment Configuration
# Generated by setup.py

# Environment
ENVIRONMENT={environment}

# Database
{"DATABASE_URL=sqlite:///./database.db" if environment == "development" else "# DATABASE_URL=postgresql://user:password@host:port/database"}

# Redis
REDIS_URL=redis://localhost:6379/0

# Security
SECRET_KEY={secret_key}
ACCESS_TOKEN_EXPIRE_MINUTES=30

# CORS (adjust for your frontend URL)
{"ALLOWED_ORIGINS=http://localhost:3000,http://127.0.0.1:3000" if environment == "development" else "# ALLOWED_ORIGINS=https://your-domain.com"}

# Logging
LOG_LEVEL=INFO
"""
    
    with open(env_path, 'w') as f:
        f.write(env_content)
    
    print(f"‚úÖ Created .env file for {environment} environment")

def setup_python_environment():
    """Set up Python virtual environment and install dependencies."""
    print("\nüêç Setting up Python environment...")
    
    # Create virtual environment if it doesn't exist
    venv_path = Path("venv")
    if not venv_path.exists():
        print("Creating virtual environment...")
        run_command(f"{sys.executable} -m venv venv")
        print("‚úÖ Virtual environment created")
    else:
        print("‚úÖ Virtual environment already exists")
    
    # Determine activation script path
    if os.name == 'nt':  # Windows
        activate_script = venv_path / "Scripts" / "activate"
        pip_path = venv_path / "Scripts" / "pip"
    else:  # Unix-like
        activate_script = venv_path / "bin" / "activate"
        pip_path = venv_path / "bin" / "pip"
    
    # Install dependencies
    print("Installing Python dependencies...")
    run_command(f"{pip_path} install --upgrade pip")
    run_command(f"{pip_path} install -r requirements.txt")
    print("‚úÖ Python dependencies installed")
    
    return activate_script

def setup_frontend():
    """Set up frontend dependencies."""
    print("\n‚öõÔ∏è  Setting up frontend...")
    
    frontend_path = Path("frontend")
    if not frontend_path.exists():
        print("‚ùå Frontend directory not found")
        return False
    
    # Install npm dependencies
    print("Installing frontend dependencies...")
    run_command("npm install", cwd=frontend_path)
    print("‚úÖ Frontend dependencies installed")
    
    # Create frontend .env file
    frontend_env_path = frontend_path / ".env.development"
    if not frontend_env_path.exists():
        with open(frontend_env_path, 'w') as f:
            f.write("VITE_API_BASE_URL=http://localhost:8000\n")
        print("‚úÖ Created frontend .env.development file")
    
    return True

